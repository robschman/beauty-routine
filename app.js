// ===== HEADER SPARKLES =====
(function() {
  const canvas = document.getElementById('header-sparkle-canvas');
  if (!canvas) return;
  const ctx = canvas.getContext('2d');
  const sparkles = [];
  const COUNT = 28;

  function resize() {
    const rect = canvas.parentElement.getBoundingClientRect();
    canvas.width = rect.width;
    canvas.height = rect.height;
  }
  resize();
  window.addEventListener('resize', resize);

  function rand(min, max) { return Math.random() * (max - min) + min; }

  function createSparkle() {
    return {
      x: rand(0, canvas.width),
      y: rand(0, canvas.height),
      size: rand(1.5, 4),
      alpha: 0,
      maxAlpha: rand(0.4, 0.95),
      speed: rand(0.008, 0.022),
      phase: Math.random() < 0.5 ? 'in' : 'out',
      delay: rand(0, 200),
    };
  }

  for (let i = 0; i < COUNT; i++) {
    const s = createSparkle();
    s.alpha = rand(0, s.maxAlpha);
    s.delay = 0;
    sparkles.push(s);
  }

  function drawStar(x, y, r, alpha) {
    ctx.save();
    ctx.globalAlpha = alpha;
    ctx.fillStyle = '#ffffff';
    ctx.shadowColor = '#fff';
    ctx.shadowBlur = r * 4;
    // 4-pointed star
    ctx.beginPath();
    for (let i = 0; i < 8; i++) {
      const angle = (i * Math.PI) / 4;
      const dist = i % 2 === 0 ? r : r * 0.35;
      const px = x + Math.cos(angle) * dist;
      const py = y + Math.sin(angle) * dist;
      i === 0 ? ctx.moveTo(px, py) : ctx.lineTo(px, py);
    }
    ctx.closePath();
    ctx.fill();
    ctx.restore();
  }

  function animate() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    sparkles.forEach(s => {
      if (s.delay > 0) { s.delay--; return; }
      if (s.phase === 'in') {
        s.alpha += s.speed;
        if (s.alpha >= s.maxAlpha) { s.alpha = s.maxAlpha; s.phase = 'out'; }
      } else {
        s.alpha -= s.speed;
        if (s.alpha <= 0) {
          Object.assign(s, createSparkle());
          s.alpha = 0;
          s.phase = 'in';
        }
      }
      drawStar(s.x, s.y, s.size, s.alpha);
    });
    requestAnimationFrame(animate);
  }
  animate();
})();

// ===== PULL TO REFRESH =====
(function() {
  let startY = 0;
  let pulling = false;
  let indicator = null;

  function createIndicator() {
    indicator = document.createElement('div');
    indicator.id = 'ptr-indicator';
    indicator.style.cssText = `
      position: fixed; top: calc(env(safe-area-inset-top) + 50px); left: 50%; transform: translateX(-50%) translateY(-2px);
      background: linear-gradient(135deg, rgba(255,182,214,0.95), rgba(255,255,255,0.95));
      backdrop-filter: blur(12px);
      border-radius: 30px; padding: 9px 22px;
      font-size: 0.78rem; color: #c2185b; font-weight: 700;
      font-family: Georgia, serif; letter-spacing: 0.3px;
      box-shadow: 0 4px 20px rgba(233,30,140,0.25), 0 0 0 1.5px rgba(233,30,140,0.15);
      transition: opacity 0.3s; opacity: 0; z-index: 9999;
      pointer-events: none; white-space: nowrap;
    `;
    indicator.textContent = '🌸 Zum Aktualisieren ziehen';
    document.body.appendChild(indicator);
  }

  document.addEventListener('touchstart', e => {
    if (window.scrollY === 0) {
      startY = e.touches[0].clientY;
      pulling = true;
      if (!indicator) createIndicator();
    }
  }, { passive: true });

  document.addEventListener('touchmove', e => {
    if (!pulling) return;
    const dy = e.touches[0].clientY - startY;
    if (dy > 10) {
      indicator.style.opacity = Math.min(1, dy / 80);
      indicator.textContent = dy > 70 ? '💜 Loslassen zum Aktualisieren' : '🌸 Zum Aktualisieren ziehen';
    }
  }, { passive: true });

  document.addEventListener('touchend', e => {
    if (!pulling) return;
    const dy = e.changedTouches[0].clientY - startY;
    if (dy > 70) {
      indicator.textContent = '✨ Wird aktualisiert…';
      indicator.style.opacity = '1';
      setTimeout(() => location.reload(), 300);
    } else {
      indicator.style.opacity = '0';
    }
    pulling = false;
    startY = 0;
  }, { passive: true });
})();
