// ===== Items =====
function getItems(catKey) {
  const isBoy = getGender() === 'boy';
  const base = (isBoy && ROUTINES_BOY[catKey]) ? ROUTINES_BOY[catKey] : (ROUTINES[catKey] || []);
  return [...base, ...(customItems[catKey] || [])];
}

function buildList(catKey) {
  const ul = document.getElementById(KEY_TO_LIST[catKey]);
  if (!ul) return;
  ul.innerHTML = '';

  getItems(catKey).forEach(item => {
    const hidden   = hiddenItems.has(item.id);
    const checked  = isChecked(item.id);
    const isCustom = (customItems[catKey] || []).some(c => c.id === item.id);

    const li = document.createElement('li');
    li.className = ['checklist-item', checked ? 'done' : '', hidden ? 'item-hidden' : ''].filter(Boolean).join(' ');
    li.dataset.id = item.id;

    const toggleTitle = hidden ? t('showItem') : t('hideItem');
    const toggleIcon  = hidden ? '👁️' : '🙈';
    const customBtns  = isCustom
      ? `<button class="action-btn edit-btn"   data-id="${item.id}" data-cat="${catKey}">✏️</button>
         <button class="action-btn delete-btn" data-id="${item.id}" data-cat="${catKey}">🗑️</button>`
      : '';

    const displayName = (currentLang === 'en' && item.en)   ? item.en     : item.name;
    const displayHint = (currentLang === 'en' && item.enHint) ? item.enHint : item.hint;

    li.innerHTML = `
      <span class="checkbox">
        <svg class="checkbox-check" viewBox="0 0 12 10">
          <polyline points="1,5 4.5,9 11,1"/>
        </svg>
      </span>
      <span class="item-emoji">${item.emoji || '📌'}</span>
      <span class="item-label">
        <span class="item-name">${escHtml(displayName)}</span>
        ${displayHint ? `<span class="item-hint">${escHtml(displayHint)}</span>` : ''}
      </span>
      <span class="item-actions">
        ${customBtns}
        <button class="action-btn toggle-btn ${hidden ? 'is-hidden' : ''}"
          data-id="${item.id}" title="${toggleTitle}">${toggleIcon}</button>
      </span>
    `;

    li.addEventListener('click', e => {
      if (editMode) return;
      if (e.target.closest('.item-actions')) return;
      toggleCheck(item.id);
    });

    ul.appendChild(li);
  });

  ul.querySelectorAll('.toggle-btn').forEach(btn =>
    btn.addEventListener('click', e => { e.stopPropagation(); toggleHidden(btn.dataset.id); })
  );
  ul.querySelectorAll('.edit-btn').forEach(btn =>
    btn.addEventListener('click', e => { e.stopPropagation(); openModal(btn.dataset.cat, btn.dataset.id); })
  );
  ul.querySelectorAll('.delete-btn').forEach(btn =>
    btn.addEventListener('click', e => { e.stopPropagation(); deleteCustomItem(btn.dataset.cat, btn.dataset.id); })
  );
}

function renderAll() { Object.keys(KEY_TO_LIST).forEach(buildList); }

function escHtml(s) {
  return s.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}

// ===== Toggle Hidden =====
function toggleHidden(id) {
  if (hiddenItems.has(id)) hiddenItems.delete(id);
  else hiddenItems.add(id);
  saveHidden();
  renderAll();
  updateProgress();
}

// ===== Custom Items =====
function deleteCustomItem(catKey, id) {
  if (!confirm(t('confirmDelete'))) return;
  customItems[catKey] = (customItems[catKey] || []).filter(i => i.id !== id);
  hiddenItems.delete(id);
  delete checkState[getTodayKey()]?.[id];
  saveCustom(); saveHidden(); saveCheck();
  renderAll(); updateProgress();
}

